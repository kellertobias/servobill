const connections: ws[] = [];
const templates = {
  html: './templates/invoice.html',
  style: './templates/invoice.css',
};
const execute = async (amountItems = -1, tax = true) => {
  const handler = new GenerateInvoiceHtmlHandler();
  const { html } = await handler.execute({
    invoice: {
      ...invoice,
      items: (amountItems >= 0 ? invoice.items.slice(0, amountItems) : invoice.items).map(
        (item) => ({
          ...item,
          taxPercentage: tax ? item.taxPercentage : 0,
        })
      ),
    },
    logoUrl: 'https://via.placeholder.com/150',
    template: fs.readFileSync(path.resolve(templates.html)).toString(),
    styles: fs.readFileSync(path.resolve(templates.style)).toString(),
    company,
  } as GenerateInvoiceHtmlCommand['request']);
  return html;
};
fs.watch(path.resolve('./templates'), { recursive: true }, (operation, file) => {
  if (!file || ![templates.html, templates.style].includes(file) || operation !== 'change') {
    return;
  }
  connections.forEach((connection) => {
    connection.send('reload');
  });
});

const port = Number.parseInt(`${process.env.PORT || 2998}`, 10);
const app = express();
const wss = new ws.Server({ port: port + 1 });

app.get('/', async (req, res) => {
  // Get query parameter ?items=1 to render only one item
  const amountItems = Number.parseInt(`${req.query?.items || -1}`, 10);
  const tax = req.query?.tax !== 'false';
  const compiledTemplate = await execute(amountItems, tax);
  res.send(
    compiledTemplate.replace(
      '<head>',
      '<head><script type="text/javascript" src="/reload.js"></script>'
    )
  );
  res.end();
});

app.get('/reload.js', (_, res) => {
  res.send(
    `
		const socket = new WebSocket('ws://localhost:${port + 1}');
		socket.addEventListener('message', (event) => {
			if (event.data === 'reload') {
				window.location.reload();
			}
		});
	`
  );
});

wss.on('connection', (ws) => {
  connections.push(ws);
  ws.send('welcome');
});

app.listen(port, () => {
  // eslint-disable-next-line no-console
  console.log(`Listening on http://localhost:${port}`);
});
